     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $category = new Category(Input::all());
        $category->user_id = $request->user()->id;
        $category->save();
        return Redirect::route('categories.index')->with('message', 'Category created');
    }

